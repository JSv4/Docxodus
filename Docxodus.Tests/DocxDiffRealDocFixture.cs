/// <summary>
/// Deterministic builder for the vendored dense bookmark / cross-reference / footnote / endnote fixture
/// (<c>TestFiles/DD/DD001-DenseBookmarkXrefFootnote.docx</c>). This is the in-repo, license-clean,
/// reproducible source-of-truth for that committed artifact: it shapes a small Series-A-style legal
/// document the way a real contract does — bookmarked defined terms and sections, REF/PAGEREF/NOTEREF
/// fields and an internal hyperlink anchor pointing back at them, footnotes (one of which cites another
/// footnote — a note-in-note reference), endnotes, and intentionally non-sequential footnote ids — so the
/// structural id↔name↔reference, body-text round-trip, and schema-validity oracles in
/// <see cref="DocxDiffBookmarkRealDocTests"/> run on every build without depending on any machine-local file.
/// <para>The committed .docx is regenerated by <c>DocxDiffBookmarkRealDocTests.__RegenerateVendoredFixture</c>
